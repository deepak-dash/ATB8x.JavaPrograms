package Sept.ex18092024;

public class Lab037 {
    public static void main(String[] args) {
        //increment ++ //decrement --
        //pre and post operator
        int a = 10;
        int b = ++a;
        System.out.println(b);
        System.out.println(a);

        System.out.println(++a);

        //post
        int c = 10;
        System.out.println(c++);
        System.out.println(c);

    }
}
